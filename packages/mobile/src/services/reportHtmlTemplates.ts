// --- 5. Full Newborn Archive ---

export function buildFullNewbornArchiveHtml(params: {
  profile: PregnancyProfile;
  feedingLogs: FeedingLog[];
  milestones: MilestoneLog[];
  diaperLogs: DiaperLog[];
  tummyTimeLogs: TummyTimeLog[];
}): string {
  const { profile, feedingLogs, milestones, diaperLogs, tummyTimeLogs } = params;
  const parentName = profile.userName || 'Parent';

  const babiesHtml = (profile.babies || [])
    .map((b) => {
      const parts = [escHtml(b.name || 'Baby'), escHtml(b.gender)];
      if (b.birthDate) parts.push('Born: ' + escHtml(b.birthDate));
      if (b.birthWeight) parts.push(b.birthWeight + ' kg');
      return `<div class="card-text">${parts.join(' - ')}</div>`;
    })
    .join('');

  const totalTummyMin = tummyTimeLogs.reduce((a, t) => a + (t.duration || 0), 0) / 60;

  const milestoneRows = [...milestones]
    .sort((a, b) => a.timestamp - b.timestamp)
    .slice(0, 50)
    .map((m) => `<tr><td>${escHtml(m.date)}</td><td>${escHtml(m.title)}</td><td>${m.notes ? escHtml(m.notes) : ''}</td></tr>`)
    .join('');

  const body = `
<div class="header">
  <h1>Nestly</h1>
  <div class="subtitle">A COMPLETE RECORD OF YOUR BABY'S FIRST DAYS</div>
  <div class="date">Newborn Archive</div>
</div>
<div class="profile-card">
  <div class="name">Parent: ${escHtml(parentName)}</div>
  <div class="detail">Family Profile</div>
</div>
${babiesHtml ? `<div class="card"><div class="card-title">The Babies</div>${babiesHtml}</div>` : ''}
<div class="section-title">Newborn Statistics</div>
<div class="stat-grid">
  <div class="stat-box">
    <div class="label">Care</div>
    <div class="row">Total Feedings: ${feedingLogs.length}</div>
    <div class="row">Total Diapers: ${diaperLogs.length}</div>
  </div>
  <div class="stat-box">
    <div class="label">Development</div>
    <div class="row">Milestones: ${milestones.length}</div>
    <div class="row">Tummy Time: ${totalTummyMin.toFixed(0)} min total</div>
  </div>
</div>
${milestoneRows ? `<div class="section-title">Milestone Timeline</div><table><thead><tr><th>Date</th><th>Milestone</th><th>Notes</th></tr></thead><tbody>${milestoneRows}</tbody></table>` : ''}`;

  return wrap("YOUR BABY'S JOURNEY - ARCHIVED BY NESTLY", body);
}
