/**
 * Case-insensitive fuzzy search across name + aliases + explanation.
 * Returns entries ranked by match quality (name prefix > name contains > alias > explanation).
 */
export function searchNutrition(query: string, limit = 8): NutritionFood[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];

  const scored: Array<{ food: NutritionFood; score: number }> = [];

  for (const food of nutritionFoods) {
    const name = food.name.toLowerCase();
    const aliases = (food.aliases ?? []).map((a) => a.toLowerCase());
    const explanation = food.explanation.toLowerCase();

    let score = 0;
    if (name === q) score = 100;
    else if (name.startsWith(q)) score = 80;
    else if (name.includes(q)) score = 60;
    else if (aliases.some((a) => a === q)) score = 70;
    else if (aliases.some((a) => a.startsWith(q))) score = 55;
    else if (aliases.some((a) => a.includes(q))) score = 40;
    else if (explanation.includes(q)) score = 20;

    if (score > 0) scored.push({ food, score });
  }

  scored.sort((a, b) => b.score - a.score || a.food.name.localeCompare(b.food.name, 'en'));
  return scored.slice(0, limit).map((s) => s.food);
}

export function getFoodById(id: string): NutritionFood | undefined {
  return nutritionFoods.find((f) => f.id === id);
}
